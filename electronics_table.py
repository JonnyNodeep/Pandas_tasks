import pandas as pd

# Создаем таблицу с товарами электроники
data = {
    'Наименование товара': ['Смартфон iPhone 13', 'Ноутбук Dell XPS 15', 'Наушники Sony WH-1000XM4', 'Планшет iPad Air', 'Smart TV Samsung'],
    'Цена': [79990, 159990, 27990, 54990, 89990],
    'Остаток': [15, 8, 25, 12, 5]
}

# Создаем DataFrame
df3 = pd.DataFrame(data)

# Выводим исходную таблицу
print("Исходная таблица:")
print(df3)
print("\nРазмер таблицы:", df3.shape)

# Добавляем новую строку с умной колонкой
new_row = pd.DataFrame({
    'Наименование товара': ['Умная колонка Amazon Alexa'],
    'Цена': [12990],
    'Остаток': [20]
})

# Объединяем исходную таблицу с новой строкой
df3 = pd.concat([df3, new_row], ignore_index=True)

# Выводим обновленную таблицу
print("\nТаблица после добавления умной колонки:")
print(df3)
print("\nНовый размер таблицы:", df3.shape)